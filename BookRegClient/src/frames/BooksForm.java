    //FUNCTION TO DISPLAY BOOKS IN TABLE
    public void showBooks() {
        try {
            Registry registry = LocateRegistry.getRegistry("localhost", 2078);
            BooksInterface book = (BooksInterface) registry.lookup("bookservice");
    
            //GETTING THE BOOKS FROM THE BOOK INTERFACE
            List result = book.getBooks();

            DefaultTableModel model = (DefaultTableModel) booksTable.getModel();
            Object[] row = new Object[3];

            for (Iterator iterator = result.iterator(); iterator.hasNext();) {
                Books bk = (Books) iterator.next();
                String name = bk.getName();
                int id = bk.getId();
                String descr = bk.getCategory();
                row[0] = id;
                row[1] = name;
                row[2] = descr;

                model.addRow(row);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            //GETTING THE CREATED REGISTRY AND ADDING THE PATH AND PORT FROM SERVER
            Registry registry = LocateRegistry.getRegistry("localhost", 2078);
            
            //LOOKING UP FOR THE REGISTRY NAME
            BooksInterface book = (BooksInterface) registry.lookup("bookservice");
            String name = nameField.getText();
            String descr = descrField.getText();
            Books bk = new Books(name, descr);
            
            //SAVING THE BOOK USING THE FUNCTION FROM THE BOOKINTERFACE
            book.saveBook(bk);
            JOptionPane.showMessageDialog(null, "Saved Successfully!");
            
            //UPDATING THE TABLE
            DefaultTableModel model = (DefaultTableModel) booksTable.getModel();
            model.setRowCount(0);
            showBooks();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_saveBtnActionPerformed

